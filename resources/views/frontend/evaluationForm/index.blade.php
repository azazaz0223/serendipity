                    });
                }
            })

            function nextStep() {
                let data = {};

                if ($("#name").val() != "") {
                    data.name = $("#name").val();
                } else {
                    Swal.fire({
                        icon: "error",
                        title: "請輸入姓名",
                        timer: 3000
                    });
                    return;
                }

                if ($("#email").val() != "") {
                    data.email = $("#email").val();
                } else {
                    Swal.fire({
                        icon: "error",
                        title: "請輸入email",
                        timer: 3000
                    });
                    return;
                }

                if ($("#phone").val() != "") {
                    data.phone = $("#phone").val();
                } else {
                    Swal.fire({
                        icon: "error",
                        title: "請輸入手機號碼",
                        timer: 3000
                    });
                    return;
                }

                localStorage.setItem(`evaluationForm`, JSON.stringify(data));

                location.href = "{{ route('frontend.evaluationForm.variety') }}";
            }
